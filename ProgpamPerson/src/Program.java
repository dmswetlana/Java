//Создание класса Person и конструкторов и методов  в нём

import java.util.*;

public class Program 
{
	//вычисляем среднне арифметическое двух чисел
	public static double average(int x, int y){
		double c = (x+y)/2D;
		return c;
	}
	
	//делаем метод для произвольного количества чисел
// public static double average(int [] m)
	public static double average(int... m){
		int summa = 0;
		for (int s : m ) 
			summa += s;
		return (double)summa/m.length;
	}
	
	
	public void sayHello(){
		//System.out.println("Привет!");
		sayHello("Незнакомец", 10);
	}
	
	public void sayHello(String name){
		System.out.printf("Привет %s!\n ", name);
	}
	
	public void sayHello(String name, int age){
		System.out.printf("Привет %s!  %d\n", name, age);
	}
	
	static void test1(int a){
		a++;
		System.out.printf("test1 вне main a=%d\n", a);
	}
	
	static void test2(int[] a){
		a[0]++;
		System.out.printf("test2 вне main a=%d\n", a[0]);
	}

	//создаём коллекцию их массива строк и сортируем её
	public static List<String> sortNames(String ... names ){
		List<String> c = Arrays.asList(names);
		Collections.sort(c);
		return c;
	}
	
	
	public static void main(String[] args) {

//так вызываем, если метод sayHello cтатический
//		sayHello();
//		Program.sayHello();
		{
			int a = 40;
			test1(a);
			System.out.println("test1 внутри main a = " + a);
		}
		
		{
			int a[] = {10};
			test2(a);
			System.out.println("test2 внутри main a = " + a[0]);
		}
		
		Program p = new Program();
		p.sayHello();
		p.sayHello("Вася");
		p.sayHello("Паша");
		p.sayHello("Костя",30);
		double a=average(15,25);
		System.out.println("Среднее = " + a);
		System.out.println("Среднее = " + average(10,10));
		//System.out.println(average(new int []{10,20,30,40}));
		System.out.println(average(10,20,30,40));
	
		for(String name: sortNames("Маша", "Ася", "Катя", "Вася"))
		System.out.println(name);
	}

}
