package ru.spec.java1.dz;

import java.util.Date;

public class Other {
	
	static {
		System.out.println("Other static init");
	}

	private int age;
	private String name;
	private Date date;
	
	
	public int getAge() {
		return this.age;
	}
	
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	
	
}
